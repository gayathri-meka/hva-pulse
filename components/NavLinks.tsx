const LF_LINKS = [
  { href: '/dashboard', label: 'Dashboard', Icon: DashboardIcon },
  { href: '/learners', label: 'My Learners', Icon: LearnersIcon },
]

export default function NavLinks({ role }: { role: 'admin' | 'LF' }) {
  const pathname = usePathname()
  const links = role === 'admin' ? ADMIN_LINKS : LF_LINKS

  return (
    <nav className="flex flex-col gap-0.5 px-3">
      {links.map(({ href, label, Icon }) => {
        const active = pathname === href || pathname.startsWith(href + '/')
        return (
          <Link
            key={href}
            href={href}
            className={`relative flex items-center gap-2.5 rounded-md px-3 py-2 text-sm font-medium transition-colors ${
              active
                ? 'bg-zinc-800 text-white'
                : 'text-zinc-400 hover:bg-zinc-800 hover:text-zinc-100'
            }`}
          >
            {active && (
              <span className="absolute left-0 inset-y-2 w-0.5 rounded-full bg-[#5BAE5B]" />
            )}
            <Icon />
            {label}
          </Link>
        )
      })}
    </nav>
  )
}
